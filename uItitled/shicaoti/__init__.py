# coding:utf-8

# int类型信息，为整型，没有带小数点
a = 22222222
print(type(a))



# float浮点数类型信息
b = 12.33
print(type(b))


# str字符串类型
c = "sd552fsf三分少" # 使用单引号或者双引号包围
print(type(c))


# list列表类型，使用中括号包围
d = [1,4,58,5,5,8,"dsf",12.22,[2,"sdf"]] # 队列
print(type(d))


# tuple元组类型，小括号包围，内容不可更改
e = (1,2,3,5,"ds","第三方")
print(type(e))


# dict字典类型，大括号包围，用冒号相等对应("key":"value")，多个值用英文逗号分开
f = {"a":"3","b":"4","c":5}
print(type(f))


# bool布尔值类型
g = True
h = False
print(type(g))
print(type(h))


# None类型，什么都没有的意思
i = None
print(type(i))